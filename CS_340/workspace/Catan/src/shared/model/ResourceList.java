package shared.model;

public class ResourceList {

	private int _brick;
	private int _ore;
	private int _sheep;
	private int _wheat;
	private int _wood;
	
	public ResourceList(){
	}

	public int get_brick() {
		return _brick;
	}

	public void set_brick(int _brick) {
		this._brick = _brick;
	}

	public int get_ore() {
		return _ore;
	}

	public void set_ore(int _ore) {
		this._ore = _ore;
	}

	public int get_sheep() {
		return _sheep;
	}

	public void set_sheep(int _sheep) {
		this._sheep = _sheep;
	}

	public int get_wheat() {
		return _wheat;
	}

	public void set_wheat(int _wheat) {
		this._wheat = _wheat;
	}

	public int get_wood() {
		return _wood;
	}

	public void set_wood(int _wood) {
		this._wood = _wood;
	}
	
	
}
